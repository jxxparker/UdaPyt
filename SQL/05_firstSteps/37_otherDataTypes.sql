# Others Useful data types

# Date : used to represent a date in the format YYYY-MM-DD
# DATE + DATETIME : YYYY-MM-DD / HH:MM:SS

# Datetime : represents the date shown on the calendar and the time shown on the clock
# TIMESTAMP : used for well-defined, exact point in time, 
# 				records the moment in time as the number of seconds passed after the 1st of January
#				represents a moment in time as a number allows you to easily obtain the difference between two TIMESTAMP values.

# BLOB : Binary Large Objects 
#		- refers to a file of binary data - data with 1s and 0s
#		- involves saving files in a record
        



